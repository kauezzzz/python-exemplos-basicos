# Variaveis
nome = "Gisele"
idade = 44

# Exibição de valores
print(nome)
print(idade)
print("\n")

# 1° método de concatenação (string: str)
print("O meu nome é: " + nome + " e tenho " + str(idade) + " anos.")

# 2° método de concatenação (format)
print("O meu nome é: {} e tenho {} anos.\n".format(nome, idade))

# 3° método de concatenação 
print(f"O meu nome é: {nome} e tenho {idade} anos.")